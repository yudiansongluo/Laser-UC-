void TIM3_Configuration(u16 per,u16 psc)
{
	TIM_TimeBaseInitTypeDef  TIM_TimeBaseStructure;
	NVIC_InitTypeDef NVIC_InitStructure;

	RCC_APB1PeriphClockCmd(RCC_APB1Periph_TIM3,ENABLE);

  TIM_TimeBaseStructure.TIM_Period = per ;
  TIM_TimeBaseStructure.TIM_Prescaler = psc ;	//设置了用来作为 TIM3 时钟频率除数的预分频值
 	TIM_TimeBaseStructure.TIM_ClockDivision = 0;	//时钟分割为0
  TIM_TimeBaseStructure.TIM_CounterMode = TIM_CounterMode_Up;//TIM3 向上计数模式

  TIM_TimeBaseInit(TIM3, &TIM_TimeBaseStructure);
	NVIC_InitStructure.NVIC_IRQChannel = TIM3_IRQn;//定时器中断通道
	NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority=2;//抢占优先级
	NVIC_InitStructure.NVIC_IRQChannelSubPriority =2;		//子优先级
	NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE;			//IRQ通道使能
	NVIC_Init(&NVIC_InitStructure);	
	TIM_Cmd(TIM3,ENABLE);    

}

